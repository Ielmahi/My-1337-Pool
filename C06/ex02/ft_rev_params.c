#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i;
	int	end;
	int	j;

	i = 0;
	j = 0;
	end = ac - 1;
	while (end >= 1)
	{
		j = 0;
		while (av[end][j])
		{
			write(1, &av[end][j], 1);
			j++;
		}
		write(1, "\n", 1);
		end--;
	}
}
